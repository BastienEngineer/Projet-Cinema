        {
            jTextField1.setText("Guest");
        }
        else
        {
            if(this.typeTarif.equals("Regular"))
            {
                jTextField1.setText("Regular");
            }
            else
            {
                if(this.typeTarif.equals("Senior"))
                {
                    jTextField1.setText("Senior");
                }
                else
                {
                    if(this.typeTarif.equals("Child"))
                    {
                        jTextField1.setText("Child");
                    }
                }
            }
        }
        MovieDAO movieDAO = new MovieDAOImpl();
