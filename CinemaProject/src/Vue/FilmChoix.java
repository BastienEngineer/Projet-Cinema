        jLabel7.setIcon(new ImageIcon(new ImageIcon(film6.getposter()).getImage().getScaledInstance(jLabel7.getWidth(),jLabel7.getHeight(),Image.SCALE_SMOOTH)));
        
        jTextField3.setText(film1.getname());
        jTextField5.setText(film2.getname());
        jTextField4.setText(film3.getname());
        jTextField2.setText(film4.getname());
        jTextField9.setText(film5.getname());
