    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jButton1.setEnabled(false);
        jSpinner1.setEnabled(false);

        JFrame f = new JFrame();
        String texte[]={"Child","Regular","Senior"};

        // create a label to display text
        for(int i=1;i<(int)jSpinner1.getValue();i++)
        {
            //a.add(new JLabel("test" + i));
            //b.add(new JComboBox());
            map.put(new JLabel("Participant " + i),new JComboBox(texte));
        }
        if((int)jSpinner1.getValue()>1)
        {
            // create a panel
            JPanel p = new JPanel();
            JButton b=new JButton("Continue");
            // add panel to frame
            f.add(p);

            /*
            for(int i=0;i<a.size();i++)
            {
                // add label to panel
                p.add(a.get(i));
            }
            */

            for(Map.Entry<JLabel,JComboBox> entry : map.entrySet())
            {
                JLabel j1=entry.getKey();
                JComboBox c2=entry.getValue();
                p.add(j1);
                p.add(c2);
            }
            p.add(b); // ajouter le bouton

            // set the size of frame
            f.setSize(400, 300);

            f.show();

            b.addActionListener(e ->
                {
                    f.dispose();
                    for(Map.Entry<JLabel,JComboBox> entry : map.entrySet())
                    {
                        JComboBox c2=entry.getValue();
                        if("Child".equals((String)c2.getSelectedItem()))
                        {
                            SetPrix(GetPrix()+5);
                        }
                        if("Senior".equals((String)c2.getSelectedItem()))
                        {
                           SetPrix(GetPrix()+7);
                        }
                        if("Regular".equals((String)c2.getSelectedItem()))
                        {
                           SetPrix(GetPrix()+12);
                        }
                    }
                });
            }
    }//GEN-LAST:event_jButton1ActionPerformed

