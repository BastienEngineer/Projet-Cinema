    public Tickets(int choixFilm,int choixSession,String typeTarif) {
        initComponents();
        this.typeTarif=typeTarif;
        MovieDAO movieDAO = new MovieDAOImpl();
        SessionDAO sessionDAO = new SessionDAOImpl();
        
       if("Child".equals(typeTarif))
        {
            prix=5;
        }
       if("Regular".equals(typeTarif))
        {
            prix=12;
        }
        if("Senior".equals(typeTarif))
        {
            prix=7;
        }
        if("Rien".equals(typeTarif))
        {
            prix=15;
        }
        
        film = movieDAO.setMovie(film,choixFilm);
        session = sessionDAO.addSession(session,choixSession);
        Calendar calendar = GregorianCalendar.getInstance();
        calendar.setTime(session.getDate());
        jTextField3.setText(film.getname());
        jTextField1.setText(session.getStart());
        jTextField2.setText(String.valueOf(calendar.get(Calendar.DAY_OF_MONTH))+"/"+(String.valueOf(calendar.get(Calendar.MONTH)+1)+"/"+calendar.get(Calendar.YEAR)));
    }

